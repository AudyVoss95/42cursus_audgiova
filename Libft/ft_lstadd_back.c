#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last;

	if (!new || !lst)
		return ;
	if (!*lst)
	{
		ft_lstadd_front(lst, new);
	}
	else
	{
		last = ft_lstlast(*lst);
		last -> next = new;
	}
}
